        //Loads all content for the game
        public Levels(Game1 game)
        {
            theGame = game;

            //Sets current level to first level
            currentLevel = 0;

            //Tiles that guns spawn on 
            gunTile = new Vectors[4];
            gunTile[0] = new Vectors(75, 87, 75);
            gunTile[1] = new Vectors(87, 75, 87);
            gunTile[2] = new Vectors(164, 115, 164);
