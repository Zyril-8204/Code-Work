            //Tiles that cannons spawn on 
            cannonTiles = new Vectors[4];
            cannonTiles[0] = new Vectors(100, 100, 100, 100);
            cannonTiles[1] = new Vectors(90, 91, 92, 93);
            cannonTiles[2] = new Vectors(150, 138, 139, 151);
            cannonTiles[3] = new Vectors(48, 36, 37, 49);

            //Enemy spawn rate for all four levels 
            mineSpawnRate = new float[4];
            mineSpawnRate[0] = 0.0f;
