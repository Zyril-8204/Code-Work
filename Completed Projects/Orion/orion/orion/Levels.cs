            mineSpawnRate[2] = 0.01f;
            mineSpawnRate[3] = 0.02f;

            //Asteroid spawn rate for all four levels 
            asteroidSpawnRate = new float[4];
            asteroidSpawnRate[0] = 0.01f;
            asteroidSpawnRate[1] = 0.01f;
            asteroidSpawnRate[2] = 0.02f;
            asteroidSpawnRate[3] = 0.01f;

            //Tile textures for all four levels
            tileTexture = new Texture2D[4];
