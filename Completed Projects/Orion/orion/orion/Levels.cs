            tileMapFileName[3] = "Content/World4.txt";   //Stub

            //Tile map initialization 
            tileMap = new tiles(20, 136, 12, 8, 64, 0, 0);
            changeLevel(currentLevel);

        }//end constructor

        public void changeAsteroidSpawnRate(float value)
        {
            Rocks.setSpawnRate(value);
        }

        /// <summary>
        /// This changes the current Level
        /// </summary>
        /// <param name="level">Must be between 0 and 3</param>
        public void changeLevel(int level)
        {
            clearLevel();   //clear all obsticles currently in level
            if (level > 3) level = 0;
            currentLevel = level;
            guns.spawnTile = gunTile[currentLevel];

            switch (currentLevel)
            {
                case 0: 
                    tileMap.loadTiles(level1Map); 
                    
                    break;
                case 1: 
                    tileMap.loadTiles(level2Map);
                    theGame.audio.musicPlay(theGame.audio.song_Level2); 
                    break;
                case 2: 
                    tileMap.loadTiles(level3Map);
                    theGame.audio.musicPlay(theGame.audio.song_Level3); 
                    break;
                case 3: 
                    tileMap.loadTiles(level4Map);
                    theGame.audio.musicPlay(theGame.audio.song_Level4); 
                    break;
            }

            playSong();

            tileMap.movePlayerToStart(); //Starts player at the bottom of the map
            Rocks.setSpawnRate(asteroidSpawnRate[currentLevel]);   //Set probability of creating Rocks
            mines.mineSpawnRate = mineSpawnRate[currentLevel];
            Rocks.setActive(true);       //Activate system

            //tileMap.yOffset = 0;
        }

        public void playSong()
        {

            if (!theGame.gamePlay)
            {
                theGame.audio.musicPlay(theGame.audio.song_Title);
            }
            else if (theGame.boss1Active || theGame.boss2Active || theGame.boss3Active || theGame.boss4Active)
            {
                theGame.audio.musicPlay(theGame.audio.song_Boss);
            }
            else
            {
                switch (currentLevel)
                {
                    case 0: theGame.audio.musicPlay(theGame.audio.song_Level1); break;
                    case 1: theGame.audio.musicPlay(theGame.audio.song_Level2); break;
                    case 2: theGame.audio.musicPlay(theGame.audio.song_Level3); break;
                    case 3: theGame.audio.musicPlay(theGame.audio.song_Level4); break;
                }
            }
        }

        public void update(Game1 game)
        {
            //Update explosions
            for (int i = 0; i < explosionList.Count; i++)
            {
                if (explosionList[i].frame < lastFrame)
                {

                    explosion temp = explosionList[i];
                    temp.frame += .25f;
                    explosionList[i] = temp;
                }
                else
                    explosionList.Remove(explosionList[i]);
            }

            //update gasCannons
            tileMap.spawnGassCannon(this, cannonTiles[currentLevel]);
            for (int i = 0; i < gassCannons.Count; i++)
            {
                gassCannons[i].update(game);

                //check for collision with bullets 
                for (int shotNumber = 0; shotNumber < game.thePlayer.thePlayerFire.Count; shotNumber++)
                {
                    if (gassCannons[i].health > 0)
                    {
                        if (Circles.overlap(new Circles(gassCannons[i].position, 32),
                            new Circles(new Vectors(game.thePlayer.thePlayerFire[shotNumber].firePosition.X, game.thePlayer.thePlayerFire[shotNumber].firePosition.Y), 2)))
                        {
                            gassCannons[i].health--;
                            game.thePlayer.thePlayerFire.Remove(game.thePlayer.thePlayerFire[shotNumber]);
                        }
                    }
                }

                //Remove gassConnons that have moved off the screen
                if (gassCannons[i].position.y > 720)
                    gassCannons.Remove(gassCannons[i]);
            }

            //Update all clouds of gas
            for (int i = 0; i < gasses.Count; i++)
            {
                gasses[i].update();
                if (gasses[i].collideWithPlayer(game, new Circles(game.thePlayer.playerPosition.X, game.thePlayer.playerPosition.Y, 0, 64))
                    && gassSpacer % 30 == 0)
                {
                    game.thePlayerHealth.rumble = true;
                    game.thePlayerHealth.checkAttack = true;    //hurt player
                }
            }

            //Allows for a pause in between each created gas plume
            gassSpacer++;
            if (gassSpacer > 100) gassSpacer = 0;

            guns.update(game, tileMap);     //Update gun system
            Rocks.update(game);             //Update rock system
            mines.Update(game, this);       //Update mine system

            if (tileMap.yOffset != 0)       //If end of tile map has not been reached...
                tileMap.yOffset += 1;       //...scroll upwards.
        }

        public void drawBackground(SpriteBatch sb, Rectangle rect)
        {
            sb.Draw(background[currentLevel], rect, Color.White);
        }

        public void drawLevel(SpriteBatch sb)
        {
            tileMap.drawTiles(sb, tileTexture[currentLevel]);   //draw tiles
            //Draw gas Cannons                                  //draw gas cannons
            for (int i = 0; i < gassCannons.Count; i++)
            {
                gassCannons[i].draw(sb, cannonSprite);
            }
            mines.draw(sb, mineSprite, explosionSprite);        //draw mines
            guns.draw(sb, gunSprite, bulletSprite);             //draw guns
            Rocks.draw(sb); //Draw Rocks                        //draw rocks
            //Draw gass                                         //draw gas
            for (int i = 0; i < gasses.Count; i++)
            {
                gasses[i].draw(sb, gassSprite);
            }
            //Draw Explosions                                   //draw Explosions
            for (int i = 0; i < explosionList.Count; i++)
            {
                sb.Draw(explosionSprite, new Rectangle((int)explosionList[i].position.x, (int)explosionList[i].position.y, 128, 128), new Rectangle((int)explosionList[i].frame * 128, 0, 128, 128), explosionList[i].color);
            }
        }

        /// <summary>
        /// Returns true if the given level has ended.
        /// </summary>
        /// <param name="level">0, 1, 2, or 3</param>
        /// <returns></returns>
        public bool isEnd(int level)
        {
            if (tileMap.isEnd() && level == currentLevel)
                return true;
            else
                return false;
        }

        //Clears the level of all obsticles 
        public void clearLevel()
        {
            //clear rocks
            Rocks.rocks = new List<Rock>();
            guns.guns = new List<gun>();
            mines.mines = new List<Circles>();
            gassCannons = new List<GassCannon>();
        }

        public void addExplosion(Vectors position, Color color)
        {
            if (explosionList.Count < 3 && color == Color.Red)
            {
                if (rnd.Next(0, 2) == 0)
                    theGame.audio.soundPlay(theGame.audio.sfx_explosion1);
                else
                    theGame.audio.soundPlay(theGame.audio.sfx_explosion2);
            }

            explosion temp;
            temp.position = position;
            temp.frame = 0;
            temp.color = color;
            explosionList.Add(temp);
        }

        public void addGass(Vectors position, Vectors direction)
        {
            gasses.Add(new Gass(position, direction));
        }
